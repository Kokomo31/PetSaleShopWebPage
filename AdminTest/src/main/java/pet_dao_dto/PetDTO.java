package pet_dao_dto;

public class PetDTO {
	private int a_num;
	private String a_name;
	private String a_health;
	private String a_gender;
	private int a_price;
	private String a_type;
	private String a_species;
	public int getA_num() {
		return a_num;
	}
	public void setA_num(int a_num) {
		this.a_num = a_num;
	}
	public String getA_name() {
		return a_name;
	}
	public void setA_name(String a_name) {
		this.a_name = a_name;
	}
	public String getA_health() {
		return a_health;
	}
	public void setA_health(String a_health) {
		this.a_health = a_health;
	}
	public String getA_gender() {
		return a_gender;
	}
	public void setA_gender(String a_gender) {
		this.a_gender = a_gender;
	}
	public int getA_price() {
		return a_price;
	}
	public void setA_price(int a_price) {
		this.a_price = a_price;
	}
	public String getA_type() {
		return a_type;
	}
	public void setA_type(String a_type) {
		this.a_type = a_type;
	}
	public String getA_species() {
		return a_species;
	}
	public void setA_species(String a_species) {
		this.a_species = a_species;
	}
	
	
}
